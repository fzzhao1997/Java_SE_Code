package com.itheima;

public class ForDemo {
    public static void main(String[] args) {
        for(int i = 1; i <= 5; i +=1 ){
            //for(初始语句(定义一个变量);
            // 条件判断语句(决定循环是否继续成立:继续,执行循环体,不成立:循环结束);
            // 条件控制语句(控制刚才定义的变量)){
            // 循环体语句(多次执行的语句);
            // }
            System.out.println("HelloWorld");
        }
    }
}
