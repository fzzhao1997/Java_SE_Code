package com.itheima.com.itheima;

public class Demo18 {
    public static void main(String[] args) {
        int i = 10;
        int j = 20;
        int k = 10;
        //等于
        System.out.println(i == j);
        System.out.println(i == k);
        System.out.println("-------");

        //不等于
        System.out.println(i != j);
        System.out.println(i != k);
        System.out.println("-------");

        //大于
        System.out.println(i > j);
        System.out.println(i > k);
        System.out.println("-------");

        //大于等于
        System.out.println(i >= j);
        System.out.println(i >= k);
        System.out.println("-------");

        //赋值
        System.out.println(i = j);//把j的值赋值给i
    }
}
